export function buildArticleDiscoveryUserPrompt(topic?: string): string {
  if (topic) {
    return `חפש מאמרים ומידע רלוונטי בנושא: ${topic}

התמקד בנושא הזה והצע 3-5 פריטי מידע שימושיים למשרד ביטוח.`;
  }

  return `הצע 3-5 מאמרים או פריטי מידע עדכניים ורלוונטיים לשוק הביטוח הישראלי.

גוון בין נושאים: רגולציה, מס, מוצרים חדשים, טיפים עונתיים, פנסיה, בריאות, רכב ורכוש.`;
}
