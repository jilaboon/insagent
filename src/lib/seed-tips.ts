export async function seedOfficeTips(): Promise<number> {
  // Check if tips already exist
  const existing = await prisma.officeTip.count();
  if (existing > 0) {
    return 0;
  }

  const created = await prisma.officeTip.createMany({
    data: RAFI_TIPS.map((tip) => ({
      title: tip.title,
      body: tip.body,
      category: tip.category,
      triggerHint: tip.triggerHint,
      isActive: true,
      createdBy: "רפי",
    })),
  });

  return created.count;
}
